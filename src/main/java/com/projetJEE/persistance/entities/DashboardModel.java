package com.projetJEE.model;

public class DashboardModel {

    private int tache;
    private int projet;
    private int equipe;
    private int chef;
    private int ressource;
    private int rapport;
    private int sousTache;
    private int calendrier;

    public int getTache() {
        return tache;
    }

    public void setTache(int tache) {
        this.tache = tache;
    }

    public int getProjet() {
        return projet;
    }

    public void setProjet(int projet) {
        this.projet = projet;
    }

    public int getEquipe() {
        return equipe;
    }

    public void setEquipe(int equipe) {
        this.equipe = equipe;
    }

    public int getChef() {
        return chef;
    }

    public void setChef(int chef) {
        this.chef = chef;
    }

    public int getRessource() {
        return ressource;
    }

    public void setRessource(int ressource) {
        this.ressource = ressource;
    }

    public int getRapport() {
        return rapport;
    }

    public void setRapport(int rapport) {
        this.rapport = rapport;
    }

    public int getSousTache() {
        return sousTache;
    }

    public void setSousTache(int sousTache) {
        this.sousTache = sousTache;
    }

    public int getCalendrier() {
        return calendrier;
    }

    public void setCalendrier(int calendrier) {
        this.calendrier = calendrier;
    }


}